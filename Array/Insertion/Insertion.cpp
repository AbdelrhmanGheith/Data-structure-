#include <iostream>
using namespace std;

int main()
{
    // تعريف المصفوفة الأصلية
    int LA[] = { 1, 3, 5, 7, 8 };
    int item = 10; // العنصر الجديد الذي سيتم إدخاله
    int k = 3;     // الموقع (بداية العد من 0) حيث سيتم إدخال العنصر
    int n = 5;     // عدد العناصر الحالية في المصفوفة
    int i = 0, j = n - 1; // i لمؤشر الحلقة، و j للحلقة العكسية لنقل العناصر

    // عرض العناصر الأصلية
    cout << "The original array elements are:\n";
    for (i = 0; i < n; i++) {
        cout << "LA[" << i << "] = " << LA[i] << "\n";
    }

    // زيادة حجم المصفوفة بمقدار 1 لإضافة عنصر جديد
    n = n + 1;

    // تحريك العناصر لإفساح المجال لإدخال العنصر الجديد
    while (j >= k) {
        LA[j + 1] = LA[j]; // نقل العنصر الحالي إلى الموقع التالي
        j = j - 1;         // الانتقال إلى العنصر السابق
    }

    // إدخال العنصر الجديد في الموقع المحدد
    LA[k] = item;

    // عرض العناصر بعد الإدخال
    cout << "\nThe array elements after insertion:\n";
    for (i = 0; i < n; i++) {
        cout << "LA[" << i << "] = " << LA[i] << "\n";
    }

    return 0;
}
